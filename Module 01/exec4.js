function experience(anos) {
    if (anos <= 1)
        console.log("Newbie")
    else if (anos <= 3)
        console.log("Mid")
    else if (anos <= 6)
        console.log("Advanced")
    else if (anos >= 7)
        console.log("Jedi Master")
}
var anosEstudo = 7;
experience(anosEstudo);
